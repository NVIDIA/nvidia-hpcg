#include <fstream>

#include "hpcg.hpp"

extern int use_output_file;
/*!
  Closes the I/O stream used for logging information throughout the HPCG run.

  @return returns 0 upon success and non-zero otherwise

  @see HPCG_Init
*/
int HPCG_Finalize(void)
{
    if (use_output_file)
        HPCG_fout.close();
    return 0;
}
